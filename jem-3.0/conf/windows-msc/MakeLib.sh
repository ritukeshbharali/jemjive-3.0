#------------------------------------------------------------------------
#   MakeLib ( %lib [%file1 %file2 ...] )
#------------------------------------------------------------------------


MakeLib()
{
  local cmd=""

  if [ -e "$1" ]; then
    cmd="lib -nologo "
  else
    cmd="lib -nologo -out:"
  fi

  eval echo "$cmd""$@"
  eval "$cmd""$@"
}
