  --with-hdf5           : enable the hdf5 package.
  --without-hdf5        : disable the hdf5 package.
                         (default: --with-hdf5)

  --hdf5-libs=LIBS      : use LIBS to link with the HDF5 libraries.
  --hdf5-libdirs=DIRS   : search in DIRS for the HDF5 libraries.
  --hdf5-incdirs=DIRS   : search in DIRS for the HDF5 header files.
